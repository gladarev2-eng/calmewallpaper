export type ProductType = 'mural' | 'panel' | 'companion';
export type PatternType = 'landscape' | 'botanical' | 'abstract' | 'architecture' | 'texture' | 'geometric';
export type RoomType = 'living' | 'bedroom' | 'office' | 'horeca' | 'hallway' | 'bathroom';

export type AspectRatio = '1:1' | '3:4' | '5:3' | '2:1' | '5:2' | '4:1';

export interface Product {
  id: string;
  name: string;
  slug: string;
  type: ProductType;
  collection: string;
  collectionId: string;
  description: string;
  shortDescription: string;
  pricePerSqm: number;
  images: string[];
  tags: string[];
  colors: string[];
  patternType: PatternType;
  roomTypes: RoomType[];
  isNew?: boolean;
  isBestseller?: boolean;
  popularity?: number;
  panelSizes?: { size: string; price: number }[];
  maxWidth?: number;
  maxHeight?: number;
  aspectRatio?: AspectRatio;
  // Color variant system
  colorVariantGroup?: string; // Group ID to link color variants together
  colorVariantName?: string;  // Name of this color variant (e.g., "Тёплый", "Холодный")
  colorVariantHex?: string;   // Dominant color hex for filter display
}

